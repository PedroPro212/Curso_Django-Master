numero = 0  # Declaramos numero com valor 0 inicial

while numero < 5:   # Enquanto numero for menor que 5, passa por dentro do While
    print(numero)   # Printa o valor de 'numero' no terminal
    numero += 1     # Acrescenta sempre +1 a variavel número, sempre que repetir

